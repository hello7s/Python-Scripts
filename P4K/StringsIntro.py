#Basic Introduction to Strings

FourC = "4C is the place 4 me!"
print(FourC)

#Escaping with single quotes
singleEscape = 'Cape Cod is "the place we can\'t help but love!"'
print(singleEscape)

#Escaping with double quotes
doubleEscape = "Cape Cod is \"the place we can't help but double love!\""
print(doubleEscape)

#Insert values into strings
myGrade = 100
display = "I got a total of %s on my test!"
print(display % myGrade)
#Change variable from myGrade to yourGrade
yourGrade = 80
print(display % yourGrade)

#Strings and multiplication
print(20 * "ha ")

